    private void enviarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarBtnActionPerformed
        
        try {
            Socket so = new Socket("192.168.1.169", 9999); //IP y puerto 
            //Socket so = new Socket("192.168.1.169", 9999); La ip ser√° del servidor
            DataOutputStream output = new DataOutputStream( so.getOutputStream()); //tunel para envio de mensaje
            output.writeUTF(chatCliente.getText()); //mensaje a enviar
            mensajeIn.append(chatCliente.getText() + "\n"); //Lo escribe en la consola general del chat
            output.close(); //cerramos el tunnel
        } catch (IOException ex) {
            System.out.println("Error en cliente conexion: " + ex.getMessage());
        }
        
        
    }//GEN-LAST:event_enviarBtnActionPerformed

